package arrays;

public class Collections {

	/*as collections tem tamanhos variáveis, podem crescer com o tempo
	 * vc cria a collection (pode ou não dizer a quantidade de elementos)
	 * e depois vai adicionando os elementos
	 * pode adicionar elementos na collection de vários tipos, mas as boas práticas dizem que é melhor escolher um tipo só
	 * as collections não suportam tipos primitivos
	 * tipos de collections: 
	 * 
	 * SET: não ordenado (por padrão), não indexado (não tem como acessar um elemento pelo índice), não aceita repetição
	 * LIST: indexado, aceita repetição
	 * MAP: estrutura chave/valor (como se fosse duas colunas, ex.: cpf e nome), CHAVE não aceita repetição mas VALOR aceita
	 * QUEUE: implementa fila, que tem a lógica first in/first out (FIFO)
	 * STACK: implementa pilha (stack), que é o last in/first out (LIFO) */

}
